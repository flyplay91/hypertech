$(document).ready(function() {

	$('.hero-slider').on('init', function(event, slick){
		$('.hero-slider.slick-initialized').css({'opacity': '1', 'visibility': 'visible'});
	});

	if ($('.hero-slider').length) {
		$('.hero-slider').on('init reInit afterChange', function(event, slick, currentSlide, nextSlide) {
	    //currentSlide is undefined on init -- set it to 0 in this case (currentSlide is 0 based)
	    var i = (currentSlide ? currentSlide : 0) + 1;
	    $('.hero-slider__nav-wrapper .status').text(i + ' / ' + slick.slideCount);
		});

		$('.hero-slider').slick({
			infinite: true,
			speed: 300,
		  	autoplay: true,
		  	autoplaySpeed: 10000,
		  slidesToShow: 1,
		  slidesToScroll: 1,
		  arrows: true,
		  appendArrows: '.hero-slider__nav-wrapper',
		  prevArrow: `<div class='prev-btn'><span class='status'></span>${iconLeft}</div>`,
		  nextArrow: `<div class='next-btn'>${iconRight}<span class='status'></span></div>`,
		});
	}

	$('.page-products-section-tabs li').on('click', function(e) {
		let idx = $(this).attr('data-content');
		$(this).addClass('active').siblings().removeClass('active');
		$('.page-products-section-content').find('.card').removeClass('active').filter('[data-tab=' + idx + ']').addClass('active');
	});

	$('.page-products-section-content .card a').on('click', function(e) {
		let href = $(this).attr('href');
		if (href.indexOf('#') == 0) {
			e.preventDefault();
			$(href).modal({
			  fadeDuration: 100
			}).on('modal:before-open', function(modal) {
				$('body').css('overflow', '');
			});
		}
	});

	
	$(document).on('change', '#dropdown-field_4', function() {
		if ($('#dropdown-field_1').val() != '-1' && $('#dropdown-field_2').val() != '-1' && $('#dropdown-field_3').val() != '-1' && $('#dropdown-field_4').val() != '-1') {
			$('#ymm_search').trigger('click');
		}
		// $('.page-ymm-custom #title').text(filterOptions);
	});

  
  	$('body').on('click', '#btnAccessoriesSection .feature-wrap', function() {
		if ($(this).closest('.polaris-powersport').hasClass('right')) {

		} else {
			$(this).closest('.polaris-powersport').removeClass('left');
			$(this).closest('.polaris-powersport').addClass('right');
			$('#shopify-section-page-products-feature-02').css('display', 'none');
			$('#shopify-section-page-products-feature-02-2').css('display', 'block');
		}
		
	});

	$('body').on('click', '#btnSpectrumSection .feature-wrap', function() {
		if ($(this).closest('.polaris-powersport').hasClass('left')) {

		} else {
			$(this).closest('.polaris-powersport').removeClass('right');
			$(this).closest('.polaris-powersport').addClass('left');
			$('#shopify-section-page-products-feature-02-2').css('display', 'none');
			$('#shopify-section-page-products-feature-02').css('display', 'block');
		}
		
	});

	
});

